class JournalEntry {
  String feeling;
  String message;
  String imageUrl;
  bool tookMedicine;
  int milesWalked;
  int painLevel;

  JournalEntry({
    required this.feeling,
    required this.message,
    required this.imageUrl,
    required this.tookMedicine,
    required this.milesWalked,
    required this.painLevel,
  });
}
